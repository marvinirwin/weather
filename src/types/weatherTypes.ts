// Base types for parameters
export interface BaseWeatherParams {
  lat: number;
  lon: number;
  units?: 'standard' | 'metric' | 'imperial';
}

export interface CurrentWeatherParams extends BaseWeatherParams {}

export interface ForecastParams extends BaseWeatherParams {}

export interface OneCallParams extends BaseWeatherParams {
  exclude?: string; // Comma-separated list of data blocks to exclude: current,minutely,hourly,daily,alerts
}

export interface AirPollutionParams {
  lat: number;
  lon: number;
}

// Geocoding parameters
export interface GeocodingParams {
  query: string;
  limit?: number;
}

export interface ReverseGeocodingParams {
  lat: number;
  lon: number;
  limit?: number;
}

// Daily Forecast parameters
export interface DailyForecastParams extends BaseWeatherParams {
  cnt?: number; // Number of days (1-16)
}

// Historical Weather parameters
export interface HistoricalWeatherParams extends BaseWeatherParams {
  dt: number; // Timestamp for historical data
}

// Solar API parameters
export interface SolarParams extends BaseWeatherParams {
  date?: string; // Date in YYYY-MM-DD format
  capacity?: number; // Solar panel capacity in kW
  angle?: number; // Panel angle
  aspect?: number; // Panel azimuth
}

// Union type for all weather card parameters
export type WeatherParams = 
  | CurrentWeatherParams 
  | ForecastParams 
  | OneCallParams 
  | AirPollutionParams
  | GeocodingParams
  | ReverseGeocodingParams
  | DailyForecastParams
  | HistoricalWeatherParams
  | SolarParams;

// Card type definition
export interface WeatherCardType {
  type: 'current' | 'forecast' | 'onecall' | 'air_pollution' | 'geocoding' | 'reverse_geocoding' | 'daily_forecast' | 'historical' | 'solar';
  parameters: WeatherParams;
  rationale?: string; // Only provided when generated by Gemini
}

// Layout definition
export interface WeatherLayout {
  cards: WeatherCardType[];
}

// Weather data response types
export interface CurrentWeatherData {
  weather: Array<{
    id: number;
    main: string;
    description: string;
    icon: string;
  }>;
  main: {
    temp: number;
    feels_like: number;
    temp_min: number;
    temp_max: number;
    pressure: number;
    humidity: number;
  };
  wind: {
    speed: number;
    deg: number;
    gust?: number;
  };
  clouds: {
    all: number;
  };
  visibility: number;
  dt: number;
  sys: {
    country: string;
    sunrise: number;
    sunset: number;
  };
  timezone: number;
  name: string;
}

