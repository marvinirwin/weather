export interface SolarData {
  current: {
    ghi: number; // Global Horizontal Irradiance (W/m²)
    dni: number; // Direct Normal Irradiance (W/m²)
    dhi: number; // Diffuse Horizontal Irradiance (W/m²)
    ghi_clear: number; // Clear-sky Global Horizontal Irradiance (W/m²)
    dni_clear: number; // Clear-sky Direct Normal Irradiance (W/m²)
    dhi_clear: number; // Clear-sky Diffuse Horizontal Irradiance (W/m²)
    cloud_coverage: number; // Cloud coverage (%)
    generation?: number; // Photovoltaic power generation (kW)
  };
  forecast?: Array<{
    date: string;
    ghi: number;
    dni: number;
    dhi: number;
    cloud_coverage: number;
    generation?: number;
  }>;
} 